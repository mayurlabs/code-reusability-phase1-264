export interface CodeLine {
  lineNum: number;
  text: string;
  matchType: 'identical' | 'similar' | 'different' | 'unique';
}

export interface DepEntry {
  name: string;
  direction: 'Inbound' | 'Outbound';
  type: string;
  status: 'Active' | 'Low Usage' | 'Shared' | 'Legacy';
  action: string;
}

export interface DependencyInfo {
  inboundCount: number;
  outboundCount: number;
  topCallers: string[];
  topDependencies: string[];
  riskLevel: 'Low' | 'Moderate' | 'High' | 'Unknown';
  migrationReady: boolean;
  details?: DepEntry[];
  actionRationale?: string;
}

export interface ClusterMember {
  id: string;
  name: string;
  surface: string;
  owner: string;
  lastModified: string;
  usageState: string;
  loc: number;
  similarity: number;
  badge: string;
  invocations30d: number;
  invocations90d: number;
  activePaths: number;
  lastObserved: string;
  runtimeConfidence: string;
  dependencies: DependencyInfo;
  codeLines?: CodeLine[];
}

export interface Cluster {
  id: string;
  name: string;
  surface: string;
  type: 'Exact Duplicate' | 'Near Duplicate' | 'Pattern Family';
  similarityRange: string;
  memberCount: number;
  runtimePriority: 'High' | 'Medium' | 'Low' | 'N/A';
  recommendation: 'Standardize' | 'Review' | 'Consolidate' | 'Retire Variant' | 'Monitor';
  owner: string;
  lastUpdated: string;
  preferredCandidate: string;
  members: ClusterMember[];
  sharedIntent: string;
  commonBlocks: string[];
  differences: string[];
  whyItMatters: string[];
  whereItAppears: string[];
  estimatedReduction: string;
  impact: {
    codeLines: string;
    workflows: string;
    changeRisk: string;
    testSurface: string;
    dependencies: string;
    governorLimits?: string;
  };
  rationale: string[];
  nextSteps: string[];
}

export interface AgentMessage {
  id: string;
  role: 'bot' | 'user';
  content: string;
  timestamp: string;
  actions?: { label: string; type: string }[];
}

